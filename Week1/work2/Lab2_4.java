//package work2;
public class Lab2_4 {
    public static void main( String args[] )
    {
        System.out.println( 123 );
        System.out.println( 123L ); //Long
        System.out.println( 0123 ); //x8
        System.out.println( 0x123 ); //x16
        System.out.println( 123.50 );
        System.out.println( 123.5f ); //Float
        System.out.println( 123e3 ); //123 * 10^3
        System.out.println( 123e-2 ); //123 * 10^-2
    }
}